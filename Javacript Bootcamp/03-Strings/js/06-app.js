// Repetir una cadena
const texto = "Hola";
const textoRepetido = texto.repeat(3);
console.log(textoRepetido); // "HolaHolaHola"

// Split una cadena en un array
const mensaje = "Hola, soy un mensaje de ejemplo";
const palabras = mensaje.split(" ");
console.log(palabras); // ["Hola,", "soy", "un", "mensaje", "de", "ejemplo"]