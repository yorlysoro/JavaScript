// Orden de los operadores
let resultado;

resultado = 20 + 30 * 2; // 20 + (30 * 2) = 20 + 60 = 80
console.log(resultado); // 80


resultado = (20 + 30) * 2; // (20 + 30) * 2 = 50 * 2 = 100
console.log(resultado); // 100


// 20% de descuento
resultado = (20 + 30 + 40 + 50 + 60) * 0.2;
console.log(resultado); // 20% de descuento: 20

//Impuesto
resultado = (20 + 30 + 40 + 50 + 60) * 0.16;
console.log(resultado); // 20% de descuento: 20, Impuesto: 16