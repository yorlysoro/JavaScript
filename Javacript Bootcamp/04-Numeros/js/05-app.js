let puntaje = 5;

console.log(puntaje); // 5

// Incremento
puntaje++; // Incrementa en 1
console.log(puntaje); // 6
// Decremento
puntaje--; // Decrementa en 1
console.log(puntaje); // 5

// Incremento con asignación
puntaje += 10; // Incrementa en 10
console.log(puntaje); // 15

// Decremento con asignación
puntaje -= 5; // Decrementa en 5
console.log(puntaje); // 10

// Multiplicación con asignación
puntaje *= 2; // Multiplica por 2
console.log(puntaje); // 20
// División con asignación
puntaje /= 4; // Divide entre 4
console.log(puntaje); // 5

// Módulo con asignación
puntaje %= 3; // Módulo 3
console.log(puntaje); // 2

// Exponenciación con asignación
puntaje **= 3; // Eleva al cubo
console.log(puntaje); // 8

// Pre-incremento
let preIncremento = 10;
console.log(++preIncremento); // 11

// Post-incremento
let postIncremento = 10;
console.log(postIncremento++); // 10
console.log(postIncremento); // 11

// Pre-decremento
let preDecremento = 10;
console.log(--preDecremento); // 9

// Post-decremento
let postDecremento = 10;
console.log(postDecremento--); // 10
console.log(postDecremento); // 9